#include <unistd.h>

void	ft_is_negative(int n)
{
	char n;
	char s;

	n = 'N';
	s = 'S';
	if (n < 0)
	{
		write(1, &n, 1);
	}
	else
	{
		write(1, &s, 1);
	}
}
