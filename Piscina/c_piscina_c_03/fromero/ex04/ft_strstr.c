char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int c;

	i = 0;
	while (str[i] != '\0')
	{
		c = 0;
		while (to_find[c] == str[i + c])
		{
			if (to_find[c + 1] == '\0')
			{
				return (str + i);
			}
			c++;
		}
		i++;
	}
	return (0);
}
